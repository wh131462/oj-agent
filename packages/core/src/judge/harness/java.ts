/**
 * Java harness 代码生成器。
 *
 * Java 没有 C 风格的文本 include,所以采用双文件 + javac 一起编的方案:
 *   - Solution.java:  来自 leetcode 的裸 class(用户编辑)
 *   - Harness.java:   含 public static void main,内部 new Solution() 调用
 *
 * Java 主类必须 public,所以 Harness 必须叫 Harness.java。LeetCode 的 snippet
 * 不带 public 修饰符,默认包私访问,Harness 与 Solution 在同一默认包中可见。
 *
 * 输入约定与 C++/Python 一致:每行一个参数 JSON 字面量。
 */

import type { HarnessSpec, HarnessType } from './spec.js';

export function generateJavaHarness(spec: HarnessSpec): string {
  if (spec.kind !== 'function') {
    throw new Error(`generateJavaHarness 不支持 ${spec.kind}: ${spec.reason}`);
  }
  const needList = usesType(spec, 'listnode');
  const needTree = usesType(spec, 'treenode');

  const lines: string[] = [];
  lines.push('// AUTO-GENERATED by oj-agent. 请勿编辑此文件,改 Solution.java 即可。');
  lines.push('import java.util.*;');
  lines.push('import java.io.*;');
  lines.push('');
  // Parser 与 Writer 始终引用 ListNode/TreeNode,因此无论题目是否用到,都注入定义。
  // 多一个未使用的 class 不影响编译或行为,但避免了 parser 内符号不全的问题。
  lines.push(JAVA_LIST_NODE);
  lines.push(JAVA_TREE_NODE);
  lines.push('');
  lines.push('public class Harness {');
  lines.push(JAVA_PARSER);
  lines.push(JAVA_WRITER);
  if (needList) lines.push(JAVA_LIST_IO);
  if (needTree) lines.push(JAVA_TREE_IO);
  lines.push('');
  lines.push('    public static void main(String[] args) throws Exception {');
  lines.push('        BufferedReader br = new BufferedReader(new InputStreamReader(System.in));');
  lines.push('        Solution sol = new Solution();');
  for (const p of spec.params) {
    const javaType = javaTypeName(p.type);
    const reader = javaReadCall(p.type);
    lines.push(`        ${javaType} ${p.name} = ${reader};`);
  }
  const args = spec.params.map((p) => p.name).join(', ');
  if (spec.returnType.tag === 'void') {
    lines.push(`        sol.${spec.funcName}(${args});`);
    lines.push('        System.out.println("null");');
  } else {
    const writer = javaWriteCall(spec.returnType, `sol.${spec.funcName}(${args})`);
    lines.push(`        System.out.println(${writer});`);
  }
  lines.push('    }');
  lines.push('}');
  return lines.join('\n') + '\n';
}

/** Java 端的类型表达式。LeetCode Java 题统一用包装类(Integer/Long)还是基本类型？
 *  实际 LeetCode 的 Java snippet 用基本类型 + int[]。我们这边输入用基本类型,
 *  vector 类型用 List<Integer> 还是 int[]？LeetCode 函数签名常见 int[] —— 但 List 更通用
 *  且 boxing 友好。这里:
 *    integer        -> int
 *    long           -> long
 *    double         -> double
 *    bool           -> boolean
 *    string         -> String
 *    char           -> char
 *    integer[]      -> int[]     (与 leetcode 签名一致)
 *    integer[][]    -> int[][]
 *    string[]       -> String[]
 *    char[]         -> char[]
 *    ListNode       -> ListNode
 *    TreeNode       -> TreeNode
 */
export function javaTypeName(t: HarnessType): string {
  switch (t.tag) {
    case 'int': return 'int';
    case 'long': return 'long';
    case 'double': return 'double';
    case 'bool': return 'boolean';
    case 'string': return 'String';
    case 'char': return 'char';
    case 'void': return 'void';
    case 'listnode': return 'ListNode';
    case 'treenode': return 'TreeNode';
    case 'array': {
      const elem = javaTypeName(t.elem);
      return `${elem}[]`;
    }
  }
}

function javaReadCall(t: HarnessType): string {
  switch (t.tag) {
    case 'int': return 'Parser.parseInt(br.readLine())';
    case 'long': return 'Parser.parseLong(br.readLine())';
    case 'double': return 'Parser.parseDouble(br.readLine())';
    case 'bool': return 'Parser.parseBool(br.readLine())';
    case 'string': return 'Parser.parseString(br.readLine())';
    case 'char': return 'Parser.parseChar(br.readLine())';
    case 'listnode': return 'Parser.parseListNode(br.readLine())';
    case 'treenode': return 'Parser.parseTreeNode(br.readLine())';
    case 'array': {
      const elem = t.elem;
      if (elem.tag === 'int') return 'Parser.parseIntArray(br.readLine())';
      if (elem.tag === 'long') return 'Parser.parseLongArray(br.readLine())';
      if (elem.tag === 'double') return 'Parser.parseDoubleArray(br.readLine())';
      if (elem.tag === 'string') return 'Parser.parseStringArray(br.readLine())';
      if (elem.tag === 'char') return 'Parser.parseCharArray(br.readLine())';
      if (elem.tag === 'bool') return 'Parser.parseBoolArray(br.readLine())';
      if (elem.tag === 'array') {
        const inner = elem.elem;
        if (inner.tag === 'int') return 'Parser.parseInt2DArray(br.readLine())';
        if (inner.tag === 'string') return 'Parser.parseString2DArray(br.readLine())';
        if (inner.tag === 'char') return 'Parser.parseChar2DArray(br.readLine())';
      }
      throw new Error(`Java harness: 不支持的数组元素类型 ${JSON.stringify(elem)}`);
    }
    case 'void': throw new Error('参数类型不能是 void');
  }
}

function javaWriteCall(t: HarnessType, expr: string): string {
  switch (t.tag) {
    case 'int':
    case 'long':
      return `String.valueOf(${expr})`;
    case 'double':
      return `Writer.formatDouble(${expr})`;
    case 'bool':
      return `((${expr}) ? "true" : "false")`;
    case 'string':
      return `Writer.quoteString(${expr})`;
    case 'char':
      return `Writer.quoteChar(${expr})`;
    case 'listnode':
      return `Writer.dumpListNode(${expr})`;
    case 'treenode':
      return `Writer.dumpTreeNode(${expr})`;
    case 'array': {
      const elem = t.elem;
      if (elem.tag === 'int') return `Writer.intArray(${expr})`;
      if (elem.tag === 'long') return `Writer.longArray(${expr})`;
      if (elem.tag === 'double') return `Writer.doubleArray(${expr})`;
      if (elem.tag === 'bool') return `Writer.boolArray(${expr})`;
      if (elem.tag === 'string') return `Writer.stringArray(${expr})`;
      if (elem.tag === 'char') return `Writer.charArray(${expr})`;
      if (elem.tag === 'array') {
        const inner = elem.elem;
        if (inner.tag === 'int') return `Writer.int2DArray(${expr})`;
        if (inner.tag === 'string') return `Writer.string2DArray(${expr})`;
        if (inner.tag === 'char') return `Writer.char2DArray(${expr})`;
      }
      throw new Error(`Java harness: 不支持的返回数组类型 ${JSON.stringify(t)}`);
    }
    case 'void': throw new Error('writeCall 不应被 void 调用');
  }
}

function usesType(
  spec: Extract<HarnessSpec, { kind: 'function' }>,
  needle: 'listnode' | 'treenode',
): boolean {
  const hit = (t: HarnessType): boolean => {
    if (t.tag === needle) return true;
    if (t.tag === 'array') return hit(t.elem);
    return false;
  };
  if (hit(spec.returnType)) return true;
  for (const p of spec.params) if (hit(p.type)) return true;
  return false;
}

