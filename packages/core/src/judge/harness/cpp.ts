/**
 * C++ harness 代码生成器。
 *
 * 给定一个 HarnessSpec（普通函数题），产出一份完整的 harness.cpp，结构：
 *
 *   1) #include <bits/stdc++.h> + using namespace std
 *   2) ListNode / TreeNode struct（按需）
 *   3) #include "solution.cpp"   ← 引入用户代码
 *   4) JSON-ish 反序列化 / 序列化辅助
 *   5) main: 按 spec.params 逐行读 stdin，调用 Solution::funcName，序列化结果到 stdout
 *
 * 输入格式约定（与 leetcode 落盘的 cases/in_<n>.txt 一致）：
 *   每行一个参数的 JSON 字面量，顺序与 params 一致。例如 twoSum：
 *     [2,7,11,15]
 *     9
 *
 * 输出格式：调用结果序列化到 stdout，末尾换行。数组用 [a,b,c]，字符串用 "x"，
 * char 用 'x'，bool 用 true/false，ListNode/TreeNode 用 [v,v,null,...]。
 * 这与题面 expected（meta.json.samples[i].output）的形式一致。
 */

import type { HarnessSpec, HarnessType } from './spec.js';

export interface CppHarnessOptions {
  /**
   * solution 文件名（相对 harness.cpp）。
   * 默认 "solution.cpp"。文本拼接 include，不参与链接。
   */
  solutionFile?: string;
}

/**
 * 生成 C++ harness 源码。spec 必须是 kind=function。
 */
export function generateCppHarness(spec: HarnessSpec, opts: CppHarnessOptions = {}): string {
  if (spec.kind !== 'function') {
    throw new Error(`generateCppHarness 不支持 ${spec.kind}: ${spec.reason}`);
  }
  const solutionFile = opts.solutionFile ?? 'solution.cpp';
  const needList = usesType(spec, 'listnode');
  const needTree = usesType(spec, 'treenode');

  const parts: string[] = [];
  parts.push('// AUTO-GENERATED by oj-agent. 请勿编辑此文件,改 solution.cpp 即可。');
  parts.push('#include <algorithm>');
  parts.push('#include <array>');
  parts.push('#include <bitset>');
  parts.push('#include <cctype>');
  parts.push('#include <climits>');
  parts.push('#include <cmath>');
  parts.push('#include <cstdint>');
  parts.push('#include <cstdio>');
  parts.push('#include <cstdlib>');
  parts.push('#include <cstring>');
  parts.push('#include <deque>');
  parts.push('#include <functional>');
  parts.push('#include <iostream>');
  parts.push('#include <list>');
  parts.push('#include <map>');
  parts.push('#include <memory>');
  parts.push('#include <numeric>');
  parts.push('#include <queue>');
  parts.push('#include <set>');
  parts.push('#include <sstream>');
  parts.push('#include <stack>');
  parts.push('#include <stdexcept>');
  parts.push('#include <string>');
  parts.push('#include <tuple>');
  parts.push('#include <unordered_map>');
  parts.push('#include <unordered_set>');
  parts.push('#include <utility>');
  parts.push('#include <vector>');
  parts.push('using namespace std;');
  parts.push('');
  if (needList) parts.push(LIST_NODE_DEF);
  if (needTree) parts.push(TREE_NODE_DEF);
  if (needList || needTree) parts.push('');
  parts.push(`#include "${solutionFile}"`);
  parts.push('');
  parts.push(READER_RUNTIME);
  parts.push('');
  if (needList) parts.push(LIST_NODE_IO);
  if (needTree) parts.push(TREE_NODE_IO);
  parts.push('');
  parts.push(generateMain(spec));
  return parts.join('\n') + '\n';
}

function generateMain(
  spec: Extract<HarnessSpec, { kind: 'function' }>,
): string {
  const lines: string[] = [];
  lines.push('int main() {');
  lines.push('    std::ios::sync_with_stdio(false);');
  lines.push('    std::string __line;');
  lines.push('    Solution __sol;');
  for (const p of spec.params) {
    lines.push('    if (!std::getline(std::cin, __line)) { std::cerr << "harness: missing input for ' + p.name + '\\n"; return 2; }');
    lines.push(`    ${cppTypeName(p.type)} ${p.name} = __oja_read<${cppTypeName(p.type)}>(__line);`);
  }
  const args = spec.params.map((p) => p.name).join(', ');
  if (spec.returnType.tag === 'void') {
    lines.push(`    __sol.${spec.funcName}(${args});`);
    lines.push('    std::cout << "null\\n";');
  } else {
    lines.push(`    auto __ret = __sol.${spec.funcName}(${args});`);
    lines.push('    __oja_write(std::cout, __ret);');
    lines.push('    std::cout << "\\n";');
  }
  lines.push('    return 0;');
  lines.push('}');
  return lines.join('\n');
}

/** HarnessType → C++ 类型表达式。 */
export function cppTypeName(t: HarnessType): string {
  switch (t.tag) {
    case 'int':
      return 'int';
    case 'long':
      return 'long long';
    case 'double':
      return 'double';
    case 'bool':
      return 'bool';
    case 'string':
      return 'std::string';
    case 'char':
      return 'char';
    case 'void':
      return 'void';
    case 'array':
      return `std::vector<${cppTypeName(t.elem)}>`;
    case 'listnode':
      return 'ListNode*';
    case 'treenode':
      return 'TreeNode*';
  }
}

function usesType(
  spec: Extract<HarnessSpec, { kind: 'function' }>,
  needle: 'listnode' | 'treenode',
): boolean {
  const hit = (t: HarnessType): boolean => {
    if (t.tag === needle) return true;
    if (t.tag === 'array') return hit(t.elem);
    return false;
  };
  if (hit(spec.returnType)) return true;
  for (const p of spec.params) if (hit(p.type)) return true;
  return false;
}

