const LIST_NODE_DEF = `struct ListNode {
    int val;
    ListNode *next;
    ListNode() : val(0), next(nullptr) {}
    ListNode(int x) : val(x), next(nullptr) {}
    ListNode(int x, ListNode *next) : val(x), next(next) {}
};`;

const TREE_NODE_DEF = `struct TreeNode {
    int val;
    TreeNode *left;
    TreeNode *right;
    TreeNode() : val(0), left(nullptr), right(nullptr) {}
    TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
    TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
};`;

/**
 * JSON-ish 字面量解析 / 序列化运行时。
 * 设计目标:
 *   - 单文件、纯标准库、无第三方依赖
 *   - 支持的字面量:
 *       int/long/double:    1   -2   1e9
 *       bool:               true / false
 *       string:             "abc\n\""
 *       char:               "a"     (LeetCode 实际写成长度 1 的字符串)
 *       vector<T>:          [v,v,v]
 *       null:               null    (用于 ListNode / TreeNode 指针反序列化的 nil)
 *   - 不严格校验,够用即可。
 */
const READER_RUNTIME = `// ----- oja harness runtime -----
namespace __oja {
struct R {
    const std::string& s;
    size_t i = 0;
    explicit R(const std::string& str) : s(str) {}
    void skip_ws() { while (i < s.size() && std::isspace((unsigned char)s[i])) ++i; }
    bool eof() { skip_ws(); return i >= s.size(); }
    char peek() { skip_ws(); return i < s.size() ? s[i] : '\\0'; }
    bool accept(char c) { skip_ws(); if (i < s.size() && s[i] == c) { ++i; return true; } return false; }
    void expect(char c) {
        skip_ws();
        if (i >= s.size() || s[i] != c) {
            throw std::runtime_error(std::string("harness: expected '") + c + "' at pos " + std::to_string(i));
        }
        ++i;
    }
    // 读 null,匹配则吞掉,返回 true
    bool try_null() {
        skip_ws();
        if (i + 4 <= s.size() && s.compare(i, 4, "null") == 0) { i += 4; return true; }
        return false;
    }
    long long read_int() {
        skip_ws();
        size_t j = i;
        if (j < s.size() && (s[j] == '-' || s[j] == '+')) ++j;
        while (j < s.size() && std::isdigit((unsigned char)s[j])) ++j;
        if (j == i) throw std::runtime_error("harness: expected int at pos " + std::to_string(i));
        long long v = std::stoll(s.substr(i, j - i));
        i = j;
        return v;
    }
    double read_double() {
        skip_ws();
        size_t j = i;
        if (j < s.size() && (s[j] == '-' || s[j] == '+')) ++j;
        while (j < s.size() && (std::isdigit((unsigned char)s[j]) || s[j] == '.' || s[j] == 'e' || s[j] == 'E' || s[j] == '+' || s[j] == '-')) ++j;
        if (j == i) throw std::runtime_error("harness: expected double at pos " + std::to_string(i));
        double v = std::stod(s.substr(i, j - i));
        i = j;
        return v;
    }
    bool read_bool() {
        skip_ws();
        if (i + 4 <= s.size() && s.compare(i, 4, "true") == 0) { i += 4; return true; }
        if (i + 5 <= s.size() && s.compare(i, 5, "false") == 0) { i += 5; return false; }
        throw std::runtime_error("harness: expected bool at pos " + std::to_string(i));
    }
    std::string read_string() {
        skip_ws();
        expect('"');
        std::string out;
        while (i < s.size() && s[i] != '"') {
            if (s[i] == '\\\\' && i + 1 < s.size()) {
                char c = s[i + 1];
                switch (c) {
                    case 'n': out.push_back('\\n'); break;
                    case 't': out.push_back('\\t'); break;
                    case 'r': out.push_back('\\r'); break;
                    case '\\\\': out.push_back('\\\\'); break;
                    case '"': out.push_back('"'); break;
                    case '/': out.push_back('/'); break;
                    case 'b': out.push_back('\\b'); break;
                    case 'f': out.push_back('\\f'); break;
                    default: out.push_back(c); break;
                }
                i += 2;
            } else {
                out.push_back(s[i]);
                ++i;
            }
        }
        expect('"');
        return out;
    }
};
} // namespace __oja

// 类型分派
template<typename T> T __oja_parse(__oja::R& r);

// 用 SFINAE 让 vector<T> 走 parse_vec
template<typename V> struct __oja_is_vec : std::false_type {};
template<typename T> struct __oja_is_vec<std::vector<T>> : std::true_type { using elem = T; };

template<typename T>
typename std::enable_if<__oja_is_vec<T>::value, T>::type
__oja_parse_dispatch(__oja::R& r);
template<typename T>
typename std::enable_if<!__oja_is_vec<T>::value, T>::type
__oja_parse_dispatch(__oja::R& r) {
    return __oja_parse<T>(r);
}

template<typename T> T __oja_read(const std::string& line) {
    __oja::R r(line);
    return __oja_parse_dispatch<T>(r);
}

template<> inline int __oja_parse<int>(__oja::R& r) { return (int)r.read_int(); }
template<> inline long long __oja_parse<long long>(__oja::R& r) { return r.read_int(); }
template<> inline double __oja_parse<double>(__oja::R& r) { return r.read_double(); }
template<> inline bool __oja_parse<bool>(__oja::R& r) { return r.read_bool(); }
template<> inline std::string __oja_parse<std::string>(__oja::R& r) { return r.read_string(); }
template<> inline char __oja_parse<char>(__oja::R& r) {
    auto s = r.read_string();
    if (s.size() != 1) throw std::runtime_error("harness: expected single char");
    return s[0];
}
template<typename T> std::vector<T> __oja_parse_vec(__oja::R& r) {
    std::vector<T> out;
    r.expect('[');
    r.skip_ws();
    if (r.accept(']')) return out;
    while (true) {
        out.push_back(__oja_parse_dispatch<T>(r));
        if (r.accept(',')) continue;
        r.expect(']');
        break;
    }
    return out;
}

template<typename T>
typename std::enable_if<__oja_is_vec<T>::value, T>::type
__oja_parse_dispatch(__oja::R& r) {
    return __oja_parse_vec<typename __oja_is_vec<T>::elem>(r);
}

// ----- 序列化 -----
inline void __oja_write(std::ostream& os, int v) { os << v; }
inline void __oja_write(std::ostream& os, long long v) { os << v; }
inline void __oja_write(std::ostream& os, double v) {
    // 与 leetcode expected 风格对齐:保留 5 位小数并 trim 尾零
    std::ostringstream ss;
    ss.setf(std::ios::fixed);
    ss.precision(5);
    ss << v;
    std::string s = ss.str();
    if (s.find('.') != std::string::npos) {
        size_t e = s.size();
        while (e > 0 && s[e - 1] == '0') --e;
        if (e > 0 && s[e - 1] == '.') --e;
        s.erase(e);
    }
    os << s;
}
inline void __oja_write(std::ostream& os, bool v) { os << (v ? "true" : "false"); }
inline void __oja_write(std::ostream& os, char c) { os << '"' << c << '"'; }
inline void __oja_write(std::ostream& os, const std::string& s) {
    os << '"';
    for (char c : s) {
        switch (c) {
            case '"': os << "\\\\\\""; break;
            case '\\\\': os << "\\\\\\\\"; break;
            case '\\n': os << "\\\\n"; break;
            case '\\t': os << "\\\\t"; break;
            case '\\r': os << "\\\\r"; break;
            default: os << c; break;
        }
    }
    os << '"';
}
template<typename T>
inline void __oja_write(std::ostream& os, const std::vector<T>& v) {
    os << '[';
    for (size_t i = 0; i < v.size(); ++i) {
        if (i) os << ',';
        __oja_write(os, v[i]);
    }
    os << ']';
}
// ----- end oja harness runtime -----`;

// 在主特化里把 vector<T> 重定向到 parse_vec —— 让用户的 __oja_parse<std::vector<X>>(r) 工作
const LIST_NODE_IO = `// ListNode I/O
inline ListNode* __oja_build_listnode(const std::vector<int>& v) {
    ListNode head(0), *tail = &head;
    for (int x : v) { tail->next = new ListNode(x); tail = tail->next; }
    return head.next;
}
template<> inline ListNode* __oja_parse<ListNode*>(__oja::R& r) {
    if (r.try_null()) return nullptr;
    auto v = __oja_parse_vec<int>(r);
    return __oja_build_listnode(v);
}
inline void __oja_write(std::ostream& os, ListNode* head) {
    os << '[';
    bool first = true;
    while (head) {
        if (!first) os << ',';
        os << head->val;
        first = false;
        head = head->next;
    }
    os << ']';
}`;

const TREE_NODE_IO = `// TreeNode I/O —— leetcode 层序 + null 占位
struct __OjaOptInt { bool has; int val; };
inline std::vector<__OjaOptInt> __oja_parse_optint_vec(__oja::R& r) {
    std::vector<__OjaOptInt> out;
    r.expect('[');
    r.skip_ws();
    if (r.accept(']')) return out;
    while (true) {
        r.skip_ws();
        if (r.try_null()) {
            out.push_back({false, 0});
        } else {
            out.push_back({true, (int)r.read_int()});
        }
        if (r.accept(',')) continue;
        r.expect(']');
        break;
    }
    return out;
}
inline TreeNode* __oja_build_treenode(const std::vector<__OjaOptInt>& v) {
    if (v.empty() || !v[0].has) return nullptr;
    TreeNode* root = new TreeNode(v[0].val);
    std::queue<TreeNode*> q; q.push(root);
    size_t i = 1;
    while (!q.empty() && i < v.size()) {
        TreeNode* cur = q.front(); q.pop();
        if (i < v.size()) {
            if (v[i].has) { cur->left = new TreeNode(v[i].val); q.push(cur->left); }
            ++i;
        }
        if (i < v.size()) {
            if (v[i].has) { cur->right = new TreeNode(v[i].val); q.push(cur->right); }
            ++i;
        }
    }
    return root;
}
template<> inline TreeNode* __oja_parse<TreeNode*>(__oja::R& r) {
    if (r.try_null()) return nullptr;
    auto v = __oja_parse_optint_vec(r);
    return __oja_build_treenode(v);
}
inline void __oja_write(std::ostream& os, TreeNode* root) {
    // 层序输出,trim 末尾连续 null
    os << '[';
    if (!root) { os << ']'; return; }
    std::vector<std::string> tokens;
    std::queue<TreeNode*> q; q.push(root);
    while (!q.empty()) {
        TreeNode* cur = q.front(); q.pop();
        if (!cur) { tokens.push_back("null"); continue; }
        tokens.push_back(std::to_string(cur->val));
        q.push(cur->left);
        q.push(cur->right);
    }
    while (!tokens.empty() && tokens.back() == "null") tokens.pop_back();
    for (size_t i = 0; i < tokens.size(); ++i) { if (i) os << ','; os << tokens[i]; }
    os << ']';
}`;
